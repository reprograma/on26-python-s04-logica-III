# for acessa a lista criada no range

# start == 0 , stop == 5, step == 1
for numero in range(5):
    print(numero)


# start == 1, stop == 5, step == 1
for numero in range(1, 5, 1):
    print(numero)
    

# start == 1, stop == 11, step == 2
for numero in range(1, 11, 2):
    print(numero)


